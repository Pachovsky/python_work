#Accessing Elements in a list:
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles[2])
print(bicycles[2].title())
print(bicycles[1].upper())
print(bicycles[0].title())
print(bicycles[-1])
print(bicycles[-2].title())

